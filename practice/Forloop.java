package practice;

public class Forloop {

	public static void main(String[] args) {
		int a = 1; // intilization
		int b =10;
		//a <= 10; condition
		//a++; increment
		for(a=1;a<=10; a++) {
			System.out.println("value of a is:"+a);
		}
//decrement
		for(b=10; b>=1;b--) {
			System.out.println("value of b is:"+b);
		}
	}

}
