 package practice;

public class Switchcasecondition {

	public static void main(String[] args) {
	int a =500;
	switch(a) {
	case 100:
		System.out.println("value is:"+ a);
		break;
	case 200:
		System.out.println("value is :"+ a);
	case 300:
		System.out.println("value is:"+ a);
	case 500:
		System.out.println("value is:"+ a);
		
		default:
		System.out.println("printing default");
	}
	

	}

}
