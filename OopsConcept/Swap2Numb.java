package OopsConcept;

public class Swap2Numb {

	public static void main(String[] args) {
		int n1= 10;
		int n2= 20;
		System.out.println("number01: "+n1);
		System.out.println("number02: "+ n2);
		int n3=n1;
		 n1=n2;
		 n2= n3;
		 System.out.println("number01: "+n1);
		 System.out.println("number02: "+ n3);

	}

}
